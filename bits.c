/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(unsigned uf) {
  unsigned power = 0xff & (uf >> 23);//Clear all bits but power
  unsigned fraction = (uf & 0x7fffff);//Mask out fraction (first 23 bits)
  unsigned powerBias = 0x7f;

  //Now check for normalization
  if (power < powerBias)
  {
    return 0x0;
  }

  //First we check for Inf cases
  if (power == 0xff){//We can find these cases by checking if the exponent is max value
    return 0x80000000u;
  }

  power = power - powerBias;

  if (power >= 31)//If there's overflow return out of range
  {
    return 0x80000000u;
  }

  unsigned retVal = fraction;

  if (power > 22)
  {
    retVal = fraction << (power - 23);
  } else {
    retVal = fraction >> (23 - power);
  }

  retVal += 1 << power;//Normalized

  if (uf >> 31)//Flip if negative
  {
    retVal = -retVal;
  } 

   return retVal;
}
