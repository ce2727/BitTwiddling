/* 
 * reverseBytes - reverse the bytes of x
 *   Example: reverseBytes(0x01020304) = 0x04030201
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3
 */
int reverseBytes(int x) {
  int reversed;
  int b1 = (x >> (24)) & 0xff;
  int b2 = (x >> (16)) & 0xff;
  int b3 = (x >> (8)) & 0xff;
  int b4 = (x >> (0)) & 0xff;
  reversed = (b4<<24) | (b3<<16) | (b2<<8) | b1;

  return reversed;
}
/* 
 * rotateRight - Rotate x to the right by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateRight(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateRight(int x, int n) {
   int mask = (1 << n)-1;//Create mask
   int eMask = (1 << (32-n))-1;
   int bringAround = (x & mask);//Get n least significant bits
   bringAround = bringAround << (32-n);//add 0's to start of the rotated bits (ex. 000000A1 -> A1000000)
   int ending = x >> n;
   ending = ending & eMask;
   int retVal = ending | bringAround;
   return retVal;
}
